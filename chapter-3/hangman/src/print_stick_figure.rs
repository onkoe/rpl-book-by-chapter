pub fn print_stick_figure(remaining_guesses: u8) {
    let parts = match remaining_guesses {
        0 => [" ", " ", " ", " ", " ", " ", " "],
        1 => ["/", " ", " ", " ", " ", " ", " "],
        2 => ["/", "|", " ", " ", " ", " ", " "],
        3 => ["/", "|", "\\"," ", " ", " ", " "],
        4 => ["/", "|", "\\","o", " ", " ", " "],
        5 => ["/", "|", "\\","o", "/", " ", " "],
        6 => ["/", "|", "\\","o", "/", "|", " "],
        _ => ["/", "|", "\\","o", "/", "|", "\\"],
    };

    println!("   +---+");
    println!("   |   |");
    println!("   {}   |", parts[0]);
    println!("  {}{}{}  |", parts[1], parts[3], parts[2]);
    println!("  {} {}  |", parts[4], parts[6]);
    println!("       |");
    println!("=========");
}
